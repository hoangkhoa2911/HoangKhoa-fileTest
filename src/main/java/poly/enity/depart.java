package poly.enity;




public class depart {

	private String id;
	private String name;
	private String exre;

	public depart(String id, String name, String exre) {
		this.id = id;
		this.name = name;
		this.exre = exre;
	
	}
	public depart() {}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	public String getExre() {
		return exre;
	}
	public void setExre(String exre) {
		this.exre = exre;
	}
	
	
	
	
	
}
